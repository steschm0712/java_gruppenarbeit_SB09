    /**
    * This method checks the correctness of the input.
    * It checks, if the program can cast the input to an integer. 
    * If it can not, the input is not a valid number(e.g. it is a string or special character).
    * The program also checks if the number is negative and will return a "false".
    */
    
    public boolean checkInput(String input){
        try { 
           int number = Integer.parseInt(input);
           if(number<0){
               return false;
           }
           return true;
       } catch(NumberFormatException e) { 
           return false; 
       } catch(NullPointerException e) {
           return false;
       }
    } 
    
    /**
    * This method gets the input from the jTextField and uses the "checkInput" Method to check the correctness.
    */
    public int getInput(){
       if(checkInput(jTextField1.getText())){
           return  Integer.parseInt(jTextField1.getText());
       }else{
           //falls Eingabe =! Int, return 0
           return 0;
       }
    }

